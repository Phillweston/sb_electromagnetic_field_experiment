u0=4*pi*1e-7;          
e0=1e-9/(36*pi);       
u1=4;
e1=1;
Z1=(u0/e0)^0.5;        
Z2=2*Z1;
x=1e8;                 
w=2*pi*x;             
k=w*(u0*e0)^0.5;       
k1=2*k;
E=50;                  
H=E/Z1;
z=0:0.01:15;
m0=zeros(size(z));
Ex=E*cos(k*z);
Hy=H*cos(k*z);
plot3(-z,Ex,m0,'g');
hold on;grid on;
plot3(-z,m0,Hy,'g');
Exx=-1/3*E*cos(k*z);
Hyx=1/3*H*cos(k*z);
plot3(-z,Exx,m0,'r');
plot3(-z,m0,Hyx,'r');
Exy=2/3*E*cos(k*z);
Hyy=2/3*H*cos(k*z);
plot3(z,Exy,m0,'k');
plot3(z,m0,Hyy,'k');
xlabel('z');
ylabel('x');
zlabel('y');
title({'平面电磁波传播垂直入射介质','绿色是入射波，红色是反射波，黑色是出射波'});